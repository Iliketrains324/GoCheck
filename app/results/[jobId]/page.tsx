              background: "transparent",
              border: "1px solid var(--hairline-strong)",
              color: "var(--ink)",
              padding: "12px 20px", borderRadius: 999,
              fontFamily: "Inter, system-ui, sans-serif", fontWeight: 600, fontSize: 14,
              textDecoration: "none",
