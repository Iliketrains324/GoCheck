// ─── Download helpers (unchanged) ────────────────────────────────────────────

async function downloadDocx(job: Job) {
  const { Document, Packer, Paragraph, TextRun, HeadingLevel, AlignmentType, BorderStyle } =
    await import("docx");

  const children: InstanceType<typeof Paragraph>[] = [];

  children.push(
    new Paragraph({
      children: [new TextRun({ text: "GoCheck — Pre-Activity Document Checklist", bold: true, size: 32 })],
      heading: HeadingLevel.TITLE,
    })
  );
  children.push(
    new Paragraph({
      children: [
        new TextRun({ text: `Generated: ${new Date().toLocaleDateString("en-PH", { year: "numeric", month: "long", day: "numeric" })}`, italics: true, size: 20, color: "666666" }),
      ],
      spacing: { after: 400 },
    })
  );

  const results = job.results ?? {};
  const entries = Object.entries(results);

  for (const [docType, result] of entries) {
    const isCoherence = docType === "COHERENCE";
    const label = isCoherence ? "Cross-Document Coherence Check" : (DOC_TYPE_LABELS[docType as DocType] ?? docType);

    children.push(
      new Paragraph({
        children: [new TextRun({ text: label, bold: true, size: 26, color: "1a3a2a" })],
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 360, after: 120 },
        border: { bottom: { style: BorderStyle.SINGLE, size: 4, color: "ccddcc" } },
      })
    );

    const statusColor = result.status === "ok" ? "2e7d32" : result.status === "error" ? "b71c1c" : "e65100";
    const statusText = result.status === "ok" ? "✓ No issues found" : result.status === "error" ? "⚠ Error" : `${result.issues.length} issue${result.issues.length !== 1 ? "s" : ""} found`;
    children.push(
      new Paragraph({
        children: [
          new TextRun({ text: "Status: ", bold: true, size: 20 }),
          new TextRun({ text: statusText, bold: true, size: 20, color: statusColor }),
          new TextRun({ text: "   " }),
          new TextRun({ text: result.summary, italics: true, size: 18, color: "555555" }),
        ],
        spacing: { after: 200 },
      })
    );

    if (result.issues.length === 0) {
      children.push(
        new Paragraph({
          children: [new TextRun({ text: "No issues to review.", italics: true, color: "888888", size: 18 })],
          spacing: { after: 200 },
        })
      );
    } else {
      for (const issue of result.issues) {
        const isMajor = issue.severity === "major";
        children.push(
          new Paragraph({
            children: [
              new TextRun({ text: "☐  ", size: 22, bold: true }),
              new TextRun({ text: `[${isMajor ? "MAJOR" : "MINOR"}] `, bold: true, size: 18, color: isMajor ? "c62828" : "7b7b7b" }),
              new TextRun({ text: issue.field, bold: true, size: 20 }),
            ],
            spacing: { before: 160, after: 60 },
          })
        );
        children.push(
          new Paragraph({
            children: [
              new TextRun({ text: "Problem: ", italics: true, size: 18, color: "333333" }),
              new TextRun({ text: issue.problem, size: 18 }),
            ],
            indent: { left: 480 },
            spacing: { after: 40 },
          })
        );
        children.push(
          new Paragraph({
            children: [
              new TextRun({ text: "Fix: ", italics: true, size: 18, color: "1b5e20" }),
              new TextRun({ text: issue.suggestion, size: 18, color: "2e7d32" }),
            ],
            indent: { left: 480 },
            spacing: { after: 160 },
          })
        );
      }
    }
  }

  children.push(
    new Paragraph({
      children: [
        new TextRun({ text: "Generated by GoCheck · gocheck-psi.vercel.app", size: 16, color: "aaaaaa", italics: true }),
      ],
      alignment: AlignmentType.CENTER,
      spacing: { before: 600 },
    })
  );

  const doc = new Document({
    creator: "GoCheck",
    title: "Pre-Activity Document Checklist",
    sections: [{ children }],
  });

  const blob = await Packer.toBlob(doc);
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `gocheck-checklist-${job.id.slice(0, 8)}.docx`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

